package com.memefest.Websockets.Decoders;

import com.fasterxml.jackson.annotation.JsonInclude;
import com.fasterxml.jackson.core.JsonProcessingException;
import com.fasterxml.jackson.databind.ObjectMapper;
import com.fasterxml.jackson.databind.type.TypeFactory;
import com.memefest.Websockets.JSON.GetJSON;

import jakarta.websocket.Decoder;
import jakarta.websocket.EndpointConfig;

public class GetDecoder implements Decoder.Text<GetJSON> {
  private ObjectMapper mapper = new ObjectMapper();
  
  public GetJSON decode(String text) {
    try {
      return (GetJSON)this.mapper.readValue(text, GetJSON.class);
    } catch (JsonProcessingException ex) {
      ex.printStackTrace();
      return null;
    } 
  }
  
  public boolean willDecode(String text) {
    return this.mapper.canDeserialize(TypeFactory.defaultInstance().constructType(GetJSON.class));
  }
  
  public void init(EndpointConfig config) {
    this.mapper.setSerializationInclusion(JsonInclude.Include.NON_NULL);
  }
}